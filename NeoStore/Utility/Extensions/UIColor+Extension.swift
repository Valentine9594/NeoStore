import UIKit

extension UIColor{
    @nonobjc class var appRed: UIColor{
//        e91c1a hex color code
        return UIColor(red: 233/255, green: 28/255, blue: 26/255, alpha: 1.0)
    }
    
    @nonobjc class var appGrey: UIColor{
        return UIColor(red: 40/255, green: 39/255, blue: 39/255, alpha: 0.7)
    }
    
    @nonobjc class var appGreyFont: UIColor{
        return UIColor(red: 51/255, green: 51/255, blue: 51/255, alpha: 1.0)
    }
}
